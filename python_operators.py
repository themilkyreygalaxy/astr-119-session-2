#Comparison operators
x = 9
y = 3
print (x==y)		#True if x equals y, False if otherwise
print (x!=y)		#True if x does not equal y, False if otherwise
print (x>y)			#True if x is greater than y, False if otherwise
print (x<y)			#True if x is less than y, False if otherwise
print (x>=y)		#Equal to or greater than y, False if otherwise
print (x<=y)		#Equal to or less than y, False if otherwise